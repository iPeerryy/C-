using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace calculadora2
{
	public class OperacionesCalculadora
	{



		public double OperacionDivision(double num1, double num2)
		{
			return num1 / num2;
		}

		public double OperacionResta(double num1, double num2)
		{
			return num1 - num2;
		}

		public double OperacionMultiplicacion(double num1, double num2)
		{
			return num1 * num2;
		}

		public double OperacionSuma(double num1, double num2)
		{
			return num1 + num2;
		}


	}

}
